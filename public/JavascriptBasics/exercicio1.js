"use strict";
// Escreva uma função que receba dois números. 
// Use o primeiro argumento como o valor percentual 
// e o segundo argumento como o valor a partir do qual a 
// porcentagem será calculada
const porcentagem = (a, b) => {
    var resultado = a * b / 100;
    return resultado;
};
porcentagem(6, 200);
